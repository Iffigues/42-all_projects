#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int i;

	i = 0;
	if (s != NULL && f)
		while (s[i])
		{
			f((unsigned)i, &s[i]);
			i++;
		}
}
